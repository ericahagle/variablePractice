// Strings
var stringOne = "string 1";
var stringTwo = "string 2";
var stringThree = "string 3";

console.log(stringOne + ", " + stringTwo + ", " + stringThree);

// Numbers
var numberOne = 1;
var numberTwo = 2;
var numberThree = 3;

console.log(numberOne + ", " + numberTwo + ", " + numberThree);

// Booleans
var booleanOne = true;
var booleanTwo = false;
var booleanThree = true;

console.log(booleanOne + ", " + booleanTwo + ", " + booleanThree);

// Reassigned string variables
stringOne = "string one";
stringTwo = "string two";
stringThree = "string three";

console.log(stringOne + ", " + stringTwo + ", " + stringThree);